<script>
	// Récupération des variables de connexion
	cle = "<?php echo $ticket; ?>";
	grade = "<?php echo $grade; ?>";
	// Affichage des différents menu en fonction de l'utilisateur
	if (cle === "Rien") {							// Si le visiteur n'est pas connecté
		$('.connecte-client').remove();
		$('.connecte-employe').remove();
		$('.connecte-admin').remove();
	} else if (cle != "Rien" && grade == 0) {		// Si le client est connecté
		$('.non-connecte').remove();
		$('.connecte-employe').remove();
		$('.connecte-admin').remove();
	} else if (cle != "Rien" && grade == 1) {		// Si l'employé est connecté
		$('.non-connecte').remove();
		$('.connecte-client').remove();
		$('.connecte-admin').remove();
	} else if (cle != "Rien" && grade == 2) {		// Si l'administrateur est connecté
		$('.non-connecte').remove();
		$('.connecte-client').remove();
		$('.connecte-employe').remove();
	}

	// Simule la touche entrée sur le formulaire pc
	$('#zone-recherche').keypress(function(e){
		if(e.which == 13){
			e.preventDefault();
			$('#submit-pc').click(); //Simule le clic sur le bouton Envoyer
		}
	});
	// Simule la touche entrée sur le formulaire mobile
	$('#zone-recherche-tel').keypress(function(e){
		if(e.which == 13){
			e.preventDefault();
			$('#submit-tel').click(); //Simule le clic sur le bouton Envoyer
		}
	});
</script>
